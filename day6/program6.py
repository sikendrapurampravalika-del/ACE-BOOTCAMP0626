#scalar Arithematic Functions
import numpy as np
arr = np.array([1,4,5,8,9])
arr1 = np.array([1,4,5,8,9])
print(np.add(arr, 2))       # Addition
print(np.subtract(arr, 2))  # Subtraction
print(np.multiply(arr, 2))  # Multiplication
print(np.divide(arr, 2))    # Division
print(np.power(arr, 2))     # Square
print(np.mod(arr, 2))       # Remainder
print((np.add(arr,arr1))) #addition
print(np.subtract(arr,arr1)) #subtraction
print(np.multiply(arr,arr1)) #multiplication
print(np.divide(arr,arr1)) #division